LOVE_WORDS = ["любовь", "отношени", "партнёр", "муж", "жена", "парень", "девушка", "чувства", "влюблён", "встреч", "расстал", "разлук", "свидан"]
WORK_WORDS = ["работа", "карьер", "бизнес", "профессии", "уволь", "повышени", "должность", "проект", "коллег", "начальник", "собеседован"]
MONEY_WORDS = ["деньги", "денег", "финанс", "зарплат", "доход", "кредит", "долг", "богатств", "инвестиц", "заработ"]


def detect_theme(question: str) -> str:
    q = question.lower()
    for w in LOVE_WORDS:
        if w in q:
            return "love"
    for w in WORK_WORDS:
        if w in q:
            return "work"
    for w in MONEY_WORDS:
        if w in q:
            return "money"
    return "default"


def pick_card(question: str) -> dict:
    q = question.lower()
    # Пробуем найти карту по ключевым словам вопроса
    scores = []
    for card in CARDS:
        score = sum(1 for kw in card["keywords"] if kw in q)
        scores.append(score)

    max_score = max(scores)
    if max_score > 0:
        # Берём все карты с максимальным счётом и выбираем по хешу
        candidates = [CARDS[i] for i, s in enumerate(scores) if s == max_score]
    else:
        candidates = CARDS

    # Детерминированный выбор по хешу вопроса
    idx = int(hashlib.md5(question.strip().lower().encode()).hexdigest(), 16) % len(candidates)
    return candidates[idx]


def handler(event: dict, context) -> dict:
    """Умный бесплатный расклад таро — анализирует вопрос и выдаёт карту с персонализированной трактовкой"""
    cors = {
        "Access-Control-Allow-Origin": "*",
        "Access-Control-Allow-Methods": "POST, OPTIONS",
        "Access-Control-Allow-Headers": "Content-Type",
    }

    if event.get("httpMethod") == "OPTIONS":
        return {"statusCode": 200, "headers": cors, "body": ""}

    body = json.loads(event.get("body") or "{}")
    question = body.get("question", "").strip()

    if not question or len(question) < 3:
        return {"statusCode": 400, "headers": cors, "body": json.dumps({"error": "question required"})}

    card = pick_card(question)
    theme = detect_theme(question)
    meaning = card["themes"][theme]

    return {
        "statusCode": 200,
        "headers": cors,
        "body": json.dumps({
            "card": {
                "name": card["name"],
                "symbol": card["symbol"],
                "energy": card["energy"],
                "meaning": meaning,
            }
        }, ensure_ascii=False),
    }
